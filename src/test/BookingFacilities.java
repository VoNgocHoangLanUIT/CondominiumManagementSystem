    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = listFacilitiesTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui l√≤ng ch·ªçn m·ªôt d√≤ng t·ª´ b·∫£ng 1.");
            return;
        }

        String soLuongText = quantityTextField.getText();
        if (soLuongText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui l√≤ng nh·∫≠p s·ªë l∆∞·ª£ng.");
            return;
        }

        try {
            // √âp ki·ªÉu v√† ki·ªÉm tra s·ªë l∆∞·ª£ng
            int soLuong = Integer.parseInt(soLuongText);

            DefaultTableModel model1 = (DefaultTableModel) listFacilitiesTable.getModel();
            DefaultTableModel model2 = (DefaultTableModel) listFacilitiesSubscribiedTable.getModel();

            // L·∫•y d·ªØ li·ªáu t·ª´ c·ªôt t∆∞∆°ng ·ª©ng c·ªßa d√≤ng ƒë∆∞·ª£c ch·ªçn
            Object cot0 = model1.getValueAt(selectedRow, 0); // c·ªôt 0
            Object cot3 = model1.getValueAt(selectedRow, 3); // c·ªôt 3
            Object cot4 = model1.getValueAt(selectedRow, 4); 
            Object cot5 = model1.getValueAt(selectedRow, 5); // c·ªôt 5
            
            boolean daTonTai = false;

            for (int i = 0; i < model2.getRowCount(); i++) {
                Object existingCot0 = model2.getValueAt(i, 0);
                if (cot0.equals(existingCot0)) {
                    // Ki·ªÉm tra s·ªë l∆∞·ª£ng t·ªìn khi c·ªông v√†o Table 2
                    if (!isQuantityValid(soLuongText, cot4)) {
                        return; // N·∫øu s·ªë l∆∞·ª£ng kh√¥ng h·ª£p l·ªá, d·ª´ng l·∫°i
                    }

                    // ƒê√£ t·ªìn t·∫°i, c·ªông th√™m s·ªë l∆∞·ª£ng
                    int soLuongHienTai = (int) model2.getValueAt(i, 1);
                    model2.setValueAt(soLuongHienTai + soLuong, i, 1);
                    daTonTai = true;
                    break;
                }
            }

            if (!daTonTai) {
                // N·∫øu ch∆∞a t·ªìn t·∫°i, ki·ªÉm tra s·ªë l∆∞·ª£ng t·ªìn tr∆∞·ªõc khi th√™m v√†o Table 2
                if (!isQuantityValid(soLuongText, cot4)) {
                    return; // N·∫øu s·ªë l∆∞·ª£ng kh√¥ng h·ª£p l·ªá, d·ª´ng l·∫°i
                }

                // Th√™m d√≤ng v√†o table 2: [c·ªôt0, textfield, c·ªôt3, null, c·ªôt5]
                model2.addRow(new Object[]{cot0, soLuong, cot3, cot5});
            }

            // Tr·ª´ s·ªë l∆∞·ª£ng t·ªìn trong table1 n·∫øu kh√¥ng ph·∫£i "unlimited"
            if (cot4 != null && !"unlimited".equalsIgnoreCase(cot4.toString().trim())) {
                try {
                    int soLuongTon = Integer.parseInt(cot4.toString());
                    int soLuongMoi = soLuongTon - soLuong;
                    
                    // C·∫≠p nh·∫≠t s·ªë l∆∞·ª£ng m·ªõi v√†o c∆° s·ªü d·ªØ li·ªáu m√† kh√¥ng th√¥ng b√°o
                    BookFacilities bookFacilities = new BookFacilities();
                    bookFacilities.updateSoLuong(cot0.toString(), soLuongMoi);
                    this.updateTableFacilities();
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "L·ªói ƒë·ªãnh d·∫°ng s·ªë l∆∞·ª£ng t·ªìn.");
                }
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "S·ªë l∆∞·ª£ng kh√¥ng h·ª£p l·ªá.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

